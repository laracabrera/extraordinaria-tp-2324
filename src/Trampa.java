public class Trampa {
    private final String descripcion;
    private final int danyo;

    public Trampa(String descripcion, int danyo) {
        this.descripcion = descripcion;
        this.danyo = danyo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public int getDanyo() {
        return danyo;
    }
}
